# perbandingan >, <, =, <= ==, !=, is, isnot

x = 3
y = 4

# lebih besar >
hasil = y > x
print (x, ">", y, "=", hasil)

# lebih kecil <
hasil = y < x
print (x, "<", y, "=", hasil)

# lebih besar >=
hasil = y >= x
print (x, ">=", y, "=", hasil)
print (x, ">=", 3, "=", hasil)


# lebih besar <=
hasil = y >= x
print (x, "<=", y, "=", hasil)
print (x, "<=", 3, "=", hasil)

# is (adalah)
hasil = x is 9
print(x, "is", 9, "=", hasil)

# ==
a = 5
b = 5
c = 10
hasil = a == b
print (a, "==", b, "=", hasil)
print (a, "==", 5, "=", hasil)
hasil = a == c
print (a, "==", c, "=", hasil)
print (a, "==", 10, "=", hasil)

# !=
a = 5
b = 5
c = 10
hasil = a != b
print (a, "!=", b, "=", hasil)
print (a, "!=", 5, "=", hasil)
hasil = a != c
print (a, "!=", c, "=", hasil)
print (a, "!=", 10, "=", hasil)






